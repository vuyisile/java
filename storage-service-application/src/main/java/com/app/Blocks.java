package com.app;

public class Blocks {
    int id;
    String blockName;
    int locationId;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getBlockName() {
        return blockName;
    }

    public void setBlockName(String block_name) {
        this.blockName = block_name;
    }

    public int getLocationId() {
        return locationId;
    }

    public void setLocation_id(int locationId) {
        this.locationId = locationId;
    }


}
